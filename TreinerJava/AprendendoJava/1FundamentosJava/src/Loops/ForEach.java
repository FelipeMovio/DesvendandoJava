package Loops;

public class ForEach {
    public static void main(String[] args) {
        System.out.println("--- Exemplo de Loop for-each ---");
        String[] frutas = {"maçã", "banana", "laranja"};
        for (String fruta : frutas) {
            System.out.println("Fruta: " + fruta);
        }

    }
}
